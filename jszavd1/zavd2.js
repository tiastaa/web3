/* 4. Необхідно підсумовувати всі непарні цілі числа в діапазоні, який користувач
введе з клавіатури.*/
let x = parseInt(prompt("Введіть початок діапазону : "));
let y = parseInt(prompt("Введіть кінець діапазону : "));
let k=0;
for(var i=x ; i<=y ; i++){
    if(i%2==1)
    {
        k=k+1;
    }
}
alert(k)